    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Connection connection =DriverManager.getConnection("jdbc:mysql://localhost:3306/universitydb","root","pc2llv2q");
            Statement st= connection.createStatement();
            ResultSet rs = st.executeQuery("select * from student where id="+jTextField1.getText());
            if(rs.next()){
                String name=rs.getString("name");
                String dept=rs.getString("dept");
                String email=rs.getString("email");
                jTextField2.setText(name);
                jTextField3.setText(email);
                jTextField4.setText(dept);
            }
        }catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

